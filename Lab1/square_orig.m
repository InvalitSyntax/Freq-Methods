%% Параметры функции
a = 4; b = 2; t0 = 1; t1 = 2; t2 = 3;
T = t2 - t0; 

%% Генерация данных (3 периода)
t = linspace(t0, t0 + 3*T, 3000); 
% Описываем логику через анонимную функцию для удобства
f_logic = @(x) a .* (mod(x-t0, T) < (t1-t0)) + b .* (mod(x-t0, T) >= (t1-t0));
f_values = f_logic(t);

%% Вызов функции отрисовки
plot_periodic_signal(t, f_values, 'f(t) - Квадратная волна', 'square_wave.pdf', [b-1, a+1]);