%% 1. Исходные параметры
a = 4; b = 2; t0 = 1; t1 = 2; t2 = 3;
T = t2 - t0;
N_harmonics = 2;

%% 2. Определение функции (f_wave)
% Используем логическое условие для формирования импульса
f_wave = @(t) a .* (mod(t-t0, T) < (t1-t0)) + b .* (mod(t-t0, T) >= (t1-t0));

%% 3. Расчет коэффициентов (вызов твоей математической функции)
[an, bn, cn, nn] = calc_fourier(f_wave, t0, T, N_harmonics);

%% 4. Вывод результатов в консоль (вызов функции печати)
print_fourier_results(an, bn, cn, nn, N_harmonics);