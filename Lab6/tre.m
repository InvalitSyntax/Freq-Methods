% ЛАБОРАТОРНАЯ РАБОТА №6
% 9. Восстановление картинки (По принципу из отчета Листинг 1)

% Очистка рабочего пространства
clear; clc; close all;

% Имена файлов
filename_orig = '15.png';
filename_edited_spectrum = 'q15_fourier_spectrum.png';
filename_output = 'q15_restored_report_method.png';

% 1. Загрузка исходного изображения (как в строке 5-6)
x = double(imread(filename_orig)) / 255;

% 2. Загрузка отредактированного спектра (как в строках 17-19)
% Спектр приводим к оттенкам серого (один канал), усредняя по 3-му измерению,
% так как вы могли сохранить его в RGB.
ed_raw = double(imread(filename_edited_spectrum)) / 255;
if size(ed_raw, 3) == 3
    ed = mean(ed_raw, 3); 
else
    ed = ed_raw;
end

% Создаем пустой массив для результата
y_final = zeros(size(x));

% Поскольку исходное фото цветное, делаем цикл по 3 каналам (RGB)
for c = 1:size(x, 3)
    
    channel = x(:,:,c);
    
    % Строка 9: Прямое преобразование Фурье и сдвиг
    F = fftshift(fft2(channel));
    
    % Строки 10-11: Находим логарифм (log1p это log(1+x)) и его максимум (L)
    S = log(abs(F) + 1);
    L = max(S(:));
    
    % Строка 20: Восстанавливаем комплексный Фурье-образ
    % exp(1i * angle(F)) - берем оригинальную фазу
    % exp(ed * L) - 1.0 - восстанавливаем закрашенную амплитуду из пикселей картинки
    fr = exp(1i * angle(F)) .* (exp(ed * L) - 1.0);
    
    % Строка 21: Обратный сдвиг, обратное преобразование Фурье и взятие вещественной части
    y = real(ifft2(ifftshift(fr)));
    
    % Строка 22: Ограничиваем значения от 0 до 1 (np.clip в питоне)
    y(y < 0) = 0;
    y(y > 1) = 1;
    
    % Сохраняем обработанный канал
    y_final(:,:,c) = y;
end

% Строка 23: Сохранение результата
imwrite(y_final, filename_output);
disp(['Готово! Изображение сохранено как: ', filename_output]);

% =========================================================================
% Отрисовка результата на экране
figure('Name', 'Восстановление по методу из отчета', 'NumberTitle', 'off', 'Position', [150, 150, 1000, 400]);

subplot(1, 3, 1);
imshow(x);
title('Оригинал');

subplot(1, 3, 2);
imshow(ed);
title('Ваш закрашенный спектр');

subplot(1, 3, 3);
imshow(y_final);
title('Результат фильтрации');