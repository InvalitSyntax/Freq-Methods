% ЛАБОРАТОРНАЯ РАБОТА №6
% 1. ФИЛЬТРАЦИЯ ИЗОБРАЖЕНИЙ С ПЕРИОДИЧНОСТЬЮ (Шаги 1-6)

% Очистка рабочего пространства
clear; clc; close all;

% Шаг 1: Загрузить изображение с помощью команды imread
filename = '15.png';
img = imread(filename);

% Шаг 2: Преобразовать массив к вещественному типу и поделить на 255
img_double = double(img) / 255;

% Шаг 3: Найти двумерный Фурье-образ массива и сдвинуть его в центр.
% Так как изображение цветное, выполняем преобразование для каждого канала.
F = zeros(size(img_double)); % Инициализируем пустой массив того же размера
if size(img_double, 3) == 3
    for c = 1:3
        F(:,:,c) = fftshift(fft2(img_double(:,:,c)));
    end
else
    % На случай, если картинка окажется черно-белой
    F = fftshift(fft2(img_double));
end

% Шаг 4: Разделить полученный образ на массивы модулей и аргументов
% с помощью функций abs и angle.
magnitude = abs(F);
arguments = angle(F); % Вычисляем аргументы (фазу), как требует пункт 4

% Шаг 5: Найти логарифм от массива модулей (+1) и нормализовать от 0 до 1
log_mag = log(magnitude + 1);

% Находим глобальные минимум и максимум для правильной нормализации всего массива
min_val = min(log_mag(:));
max_val = max(log_mag(:));

% Нормализуем по формуле: (X - min) / (max - min)
log_mag_norm = (log_mag - min_val) / (max_val - min_val);

% Шаг 6: Сохранить полученный массив как изображение командой imwrite
output_filename = '15_fourier_spectrum.png';
imwrite(log_mag_norm, output_filename);

% =========================================================================
% Опционально: Вывод на экран, чтобы вы сразу увидели результат (согласно 
% ожидаемым результатам из п. 1.2 методички)
figure('Name', 'Многомерное преобразование Фурье', 'NumberTitle', 'off');

subplot(1, 2, 1);
imshow(img);
title('1. Исходное изображение');

subplot(1, 2, 2);
imshow(log_mag_norm);
title('2. Логарифм модуля Фурье-образа');

disp(['Готово! Фурье-образ сохранен в файл: ', output_filename]);